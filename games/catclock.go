const (
	W = 400
	H = 400

	C = 16

	ML = 30
	HL = 20
	HW = 4

	FPS = 1000 / (C * 2) * time.Millisecond
)

var (
	crossedEye = flag.Bool("c", false, "crossed eye cat")

	tailSize = sdl.Point{150, 89}
	eyeSize  = sdl.Point{56, 23}

	window    *sdl.Window
	renderer  *sdl.Renderer
	cat, eyes *sdl.Texture
	tail, eye []*sdl.Texture
	hand      struct {
		minute, hour *sdl.Texture
	}
	cycle = struct {
		t, dt int
	}{0, 1}
)

func main() {
	log.SetFlags(0)
	runtime.LockOSThread()
	flag.Parse()

	err := sdl.Init(sdl.INIT_VIDEO | sdl.INIT_TIMER)
	ck(err)
	defer sdl.Quit()

	sdl.SetHint(sdl.HINT_RENDER_SCALE_QUALITY, "best")
	window, renderer, err = sdl.CreateWindowAndRenderer(W, H, sdl.WINDOW_RESIZABLE)
	ck(err)

	renderer.SetLogicalSize(W, H)
	window.SetTitle("Cat Clock")

	cat = loadImage(catb)
	eyes = loadImage(eyesb)
	hand.minute = makeTexture(ML*2, ML*2)
	hand.hour = makeTexture(HL+5, HL+5)
	for i := 0; i <= C; i++ {
		tail = append(tail, renderTail(float64(i)*math.Pi/C))
		eye = append(eye, renderEye(float64(i)*math.Pi/C))
	}

	renderer.SetTarget(nil)
	ticker := time.Tick(FPS)
	for {
		event()
		select {
		case <-ticker:
			update()
		default:
		}
		draw()
	}
}

func makeTexture(w, h int) *sdl.Texture {
	texture, err := renderer.CreateTexture(
		sdl.PIXELFORMAT_ARGB8888,
		sdl.TEXTUREACCESS_STATIC|sdl.TEXTUREACCESS_TARGET,
		w, h,
	)
	ck(err)

	err = renderer.SetTarget(texture)
	ck(err)

	renderer.SetDrawColor(sdlcolor.White)
	renderer.Clear()
	return texture
}

func loadImage(m image.Image) *sdl.Texture {
	texture, err := sdlimage.LoadTextureImage(renderer, m)
	ck(err)
	return texture
}

func event() {
	for {
		ev := sdl.PollEvent()
		if ev == nil {
			break
		}
		switch ev := ev.(type) {
		case sdl.QuitEvent:
			os.Exit(0)
		case sdl.KeyDownEvent:
			switch ev.Sym {
			case sdl.K_ESCAPE:
				os.Exit(0)
			}
		}
	}
}

func draw() {
	renderer.SetDrawColor(sdlcolor.White)
	renderer.Clear()

	catOff := sdl.Rect{
		(W - catb.W) / 2,
		(H - catb.H) / 2,
		catb.W,
		catb.H,
	}

	off := sdl.Point{49, 30}
	eyeOff := sdl.Rect{
		catOff.X + off.X,
		catOff.Y + off.Y,
		eyeSize.X,
		eyeSize.Y,
	}

	off = sdl.Point{0, 211}
	tailOff := sdl.Rect{
		catOff.X + off.X,
		catOff.Y + off.Y,
		tailSize.X,
		tailSize.Y,
	}

	renderer.Copy(cat, nil, &catOff)
	renderer.Copy(eye[cycle.t], nil, &eyeOff)
	renderer.Copy(tail[cycle.t], nil, &tailOff)

	now := time.Now()
	hour := float64(now.Hour() % 12)
	min := float64(now.Minute())

	phase := 2 * math.Pi * (min - 15) / 60
	drawHand(hand.minute, ML, HW, phase, 8, sdl.Color{150, 150, 150, 255})

	phase = 2 * math.Pi * ((hour - 3) + min/60) / 12
	drawHand(hand.hour, HL, HW, phase, 5, sdlcolor.Black)

	renderer.Present()
}

func update() {
	cycle.t += cycle.dt
	if cycle.t < 0 || cycle.t > C {
		cycle.t -= 2 * cycle.dt
		cycle.dt = -cycle.dt
	}
}

func drawHand(hand *sdl.Texture, length float64, width int, phase, scale float64, col sdl.Color) {
	si, co := math.Sincos(phase)

	c := ga.Vec2d{W/2 - 2, H / 2}
	p := ga.Vec2d{c.X + co*length, c.Y + si*length}
	d := vec2.Perp(vec2.Sub(p, c))
	d = vec2.Scale(vec2.Normalize(d), scale)
	q := vec2.Add(c, d)
	r := vec2.Sub(c, d)

	sdlgfx.FilledTrigon(renderer, int(p.X), int(p.Y), int(q.X), int(q.Y), int(r.X), int(r.Y), col)
}

func ck(err error) {
	if err != nil {
		log.Fatal("catclock:", err)
	}
}

func renderTail(t float64) *sdl.Texture {
	toffs := sdl.Point{74, 0}
	tp := []sdl.Point{
		{0, 0},
		{0, 76},
		{3, 82},
		{10, 84},
		{18, 82},
		{21, 76},
		{21, 70},
	}

	rtp := make([]sdl.Point, len(tp))
	phase := .4*math.Sin(t+3*math.Pi/2) - 0.08
	s, c := math.Sincos(phase)
	for i, tp := range tp {
		fx := float64(tp.X)
		fy := float64(tp.Y)
		x := fx*c + fy*s
		y := -fx*s + fy*c
		rtp[i] = sdl.Point{int32(x), int32(y)}.Add(toffs)
	}
	texture := makeTexture(int(tailSize.X), int(tailSize.Y))
	fillPolygon(texture, rtp)
	return texture
}

func calcEye(u, phase, sign float64) sdl.Point {
	o := sign * math.Pi / 7
	x := math.Cos(u) * math.Cos(phase+o)
	y := math.Sin(u)
	z := 2 + math.Cos(u)*math.Sin(phase+o)
	if z != 0 {
		x /= z
		y /= z
	}
	return sdl.Point{int32(x*23 + 12), int32(y*23 + 11)}
}

func renderEye(t float64) *sdl.Texture {
	texture := makeTexture(int(eyeSize.X), int(eyeSize.Y))
	renderer.Copy(eyes, nil, nil)

	var pts []sdl.Point
	phase := 0.7*math.Sin(t+3*math.Pi/2) + math.Pi/2
	for u := -math.Pi / 2; u < math.Pi/2; u += .25 {
		pts = append(pts, calcEye(u, phase, 1))
	}
	for u := math.Pi / 2; u > -math.Pi/2; u -= 0.25 {
		pts = append(pts, calcEye(u, phase, -1))
	}
	fillPolygon(texture, pts)

	if *crossedEye {
		pts = pts[:0]
		phase = 0.7*math.Sin(math.Pi-t+3*math.Pi/2) + math.Pi/2
		for u := -math.Pi / 2; u < math.Pi/2; u += 0.25 {
			pts = append(pts, calcEye(u, phase, 1))
		}
		for u := math.Pi / 2; u > -math.Pi/2; u -= 0.25 {
			pts = append(pts, calcEye(u, phase, -1))
		}
	}
	for i := range pts {
		pts[i].X += 31
	}
	fillPolygon(texture, pts)

	return texture
}

func fillPolygon(texture *sdl.Texture, pts []sdl.Point) {
	ck(renderer.SetTarget(texture))
	sdlgfx.FilledPolygon(renderer, pts, sdlcolor.Black)
}

type Bitmap struct {
	sdl.Rect
	Pix []uint8
}

func (b *Bitmap) Bounds() image.Rectangle {
	return image.Rect(0, 0, int(b.W), int(b.H))
}

func (b *Bitmap) ColorModel() color.Model {
	return color.GrayModel
}

func (b *Bitmap) At(x, y int) color.Color {
	w := int(b.W)
	s := (y*w + x) / 8
	o := (y*w + x) % 8
	if b.Pix[s]&(1<<uint(7-o)) != 0 {
		return color.Gray{0}
	}
	return color.Gray{255}
}

func (b *Bitmap) Set(x, y int, c color.Color) {
	w := int(b.W)
	s := (y*w + x) / 8
	o := (y*w + x) % 8
	cr, cg, cb, _ := c.RGBA()
	if cr != 0 || cg != 0 || cb != 0 {
		b.Pix[s] |= (1 << uint(7-o))
	} else {
		b.Pix[s] &^= (1 << uint(7-o))
	}
}

func init() {
	r := eyesb.Bounds()
	for y := r.Min.Y; y < r.Max.Y; y++ {
		for x := r.Dx() - 2; x < r.Max.X; x++ {
			eyesb.Set(x, y, color.Gray{255})
		}
	}
}

